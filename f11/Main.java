public class Main {
    public static void main(String[] args) {
        String str = ""; // Initialize an empty string
        
        // Assign a new string to the same variable multiple times
        str = "Hello";
        System.out.println("String 1: " + str);
        
        str = "World";
        System.out.println("String 2: " + str);
        
        str = "Java";
        System.out.println("String 3: " + str);
    }
}